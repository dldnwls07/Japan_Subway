// 외래어 표기법 표기 세칙 유의사항: ㅈ/ㅊ 뒤에 오는 이중모음(ㅑㅕㅖㅛㅠ)은 단모음으로 적는다
// (예: 쥬스(X)->주스(O), 규슈 → 쵸(X)->초(O)). ㅈ/ㅊ은 한국어에서 이미 구개음이라
// 뒤따르는 반모음(y-glide)이 음운적으로 구별되지 않기 때문이다. じゅ/ちょ 등 요음이
// 이 규칙의 대상이라 buildMap에서 일괄 적용한다.
const GLIDE_SIMPLIFICATION_AFTER_AFFRICATE: Record<string, string> = {
  "ㅑ": "ㅏ", "ㅕ": "ㅓ", "ㅖ": "ㅔ", "ㅛ": "ㅗ", "ㅠ": "ㅜ",
};

function normalizeChoJung([cho, jung]: ChoJung): ChoJung {
  if ((cho === "ㅈ" || cho === "ㅊ" || cho === "ㅉ") && GLIDE_SIMPLIFICATION_AFTER_AFFRICATE[jung]) {
    return [cho, GLIDE_SIMPLIFICATION_AFTER_AFFRICATE[jung]];
  }
  return [cho, jung];
}

function buildMap(entries: readonly (readonly [string, string, ChoJung])[]): Record<string, string> {
  const map: Record<string, string> = {};
  for (const [hira, kata, chojung] of entries) {
    const hangul = composeSyllable(...normalizeChoJung(chojung));
    map[hira] = hangul;
    map[kata] = hangul;
  }
  return map;
}

const BASE_MAP = buildMap(BASE_ENTRIES);

export const INITIAL_MAP: Record<string, string> = {
  ...BASE_MAP,
  ...buildMap(POSITIONAL_ENTRIES.map(([hira, kata, initial]) => [hira, kata, initial] as const)),
};

export const MEDIAL_MAP: Record<string, string> = {
  ...BASE_MAP,
  ...buildMap(POSITIONAL_ENTRIES.map(([hira, kata, , medial]) => [hira, kata, medial] as const)),
};

